
public class DatatypesExample {
	public static void main(String[] args) {
		//boolean stores true or false
		boolean bo = true;
		System.out.println(bo);
		//Byte it stores values from -128 to 127
		byte by;
		by = 124;
		System.out.println(by);
		//short stores from -32768 to 32767
		short sh=-500;
		System.out.println(sh);
		//int stores from -2^31 to 2^31-1
		int no = 124324;
		System.out.println(no);
		//long stores from -2^63 to 2^63 -1
		long lo= 433333322;
		System.out.println(lo);
		//double is double precision 64-bit currency
		double dou = 2343.2;
		System.out.println(dou);
		//float is a single-precision 32bit floating point
		float fl = -40.2f;
		System.out.println(fl);
		//char 
		char ch = 'a';
		System.out.println(ch);
		//string
		String str = "Parth Shukla";
		System.out.println(str);
		
	}
}
